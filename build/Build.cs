using System;
using System.IO;
using System.Linq;
using System.Text.Json;
using System.Text.RegularExpressions;
using Nuke.Common;
using Nuke.Common.IO;
using Nuke.Common.Tooling;
using Nuke.Common.Tools.DotNet;
using Nuke.Common.Tools.Npm;
using Serilog;
using static Nuke.Common.Tools.DotNet.DotNetTasks;
using static Nuke.Common.Tools.Git.GitTasks;
using static Nuke.Common.Tools.Npm.NpmTasks;

/// <summary>
/// Build entry point for <c>@ancplua/qyl-api-schema</c>.
///
/// Owns qyl-api-schema's restore, compile, deterministic-emission, and package gates.
/// </summary>
sealed class Build : NukeBuild
{
    public static int Main() => Execute<Build>(x => x.EmitAll);

    const string OtelKeysVersion = "1.43.0";
    static readonly string[] Emitters = ["csharp", "ts-types"];
    AbsolutePath DomainSpecRoot => RootDirectory;
    AbsolutePath EmitOutputDir => RootDirectory / "Artifacts" / "emit";

    AbsolutePath ArtifactsDir => RootDirectory / "Artifacts";

    // ---------------------------------------------------------------------
    // RestoreTypeSpecDeps: npm ci
    // ---------------------------------------------------------------------
    Target RestoreTypeSpecDeps => _ => _
        .Description("Restore npm dependencies (npm ci) for the TypeSpec spec.")
        .Executes(() =>
        {
            NpmCi(s => s.SetProcessWorkingDirectory(DomainSpecRoot));
        });

    // ---------------------------------------------------------------------
    // VerifyKeysLockstep: pin assertion. generated/otel-keys.gen.tsp is
    // regenerated by the Qyl.OpenTelemetry.SemanticConventions repo's Weaver
    // pipeline (src/…SourceGeneration/scripts/emit_typespec_keys.py) from core
    // semconv + the GenAI dev registry; assert its header pins core semconv
    // v{OtelKeysVersion} so the TypeSpec and C# surfaces cannot drift apart.
    // ---------------------------------------------------------------------
    Target VerifyKeysLockstep => _ => _
        .Description("Assert generated/otel-keys.gen.tsp is pinned at core semconv OtelKeysVersion.")
        .DependsOn(RestoreTypeSpecDeps)
        .Executes(() =>
        {
            var spec = DomainSpecRoot;
            var expected = OtelKeysVersion;

            var committed = spec / "generated" / "otel-keys.gen.tsp";

            if (!File.Exists(committed))
            {
                throw new InvalidOperationException(
                    $"VerifyKeysLockstep: committed keys file '{committed}' is missing.");
            }

            if (!string.IsNullOrWhiteSpace(expected))
            {
                var pin = $"open-telemetry/semantic-conventions v{expected} ";
                if (!File.ReadAllText(committed).Contains(pin, StringComparison.Ordinal))
                {
                    throw new InvalidOperationException(
                        $"VerifyKeysLockstep: '{committed}' header does not pin '{pin.TrimEnd()}' (OtelKeysVersion='{expected}'). " +
                        "Regenerate via the Qyl.OpenTelemetry.SemanticConventions repo's emit_typespec_keys.py or fix the pin.");
                }
            }

            Log.Information(
                "VerifyKeysLockstep: '{Committed}' pinned at core semconv v{Version}.",
                committed, expected);
        });

    // ---------------------------------------------------------------------
    // CompileDomainSpec: npm run lint:public (tsp compile index.tsp --no-emit --warn-as-error)
    // ---------------------------------------------------------------------
    Target CompileDomainSpec => _ => _
        .Description("Compile the published TypeSpec surface (index.tsp) with --no-emit --warn-as-error via `npm run lint:public`.")
        .DependsOn(VerifyKeysLockstep)
        .Executes(() =>
        {
            var spec = DomainSpecRoot;
            NpmRun(s => s
                .SetCommand("lint:public")
                .SetProcessWorkingDirectory(spec));
        });

    // ---------------------------------------------------------------------
    // Per-emitter targets: npx tsp compile main.tsp --emit <emitter> --output-dir <out>
    // ---------------------------------------------------------------------
    Target EmitCSharp => _ => _
        .Description("Run the C# TypeSpec emitter into Artifacts/emit/csharp.")
        .DependsOn(CompileDomainSpec)
        .Executes(() => RunTypeSpecEmitter("@ancplua/typespec-emit-csharp", "csharp"));

    Target EmitTsTypes => _ => _
        .Description("Run the TypeScript-types TypeSpec emitter into Artifacts/emit/ts-types.")
        .DependsOn(CompileDomainSpec)
        .Executes(() => RunTypeSpecEmitter("@ancplua/typespec-emit-ts-types", "ts-types"));

    Target EmitAll => _ => _
        .Description("Run the C# and TypeScript contract emitters.")
        .DependsOn(
            EmitCSharp,
            EmitTsTypes);

    void RunTypeSpecEmitter(string emitterPackage, string outputSubdir)
        => RunTypeSpecEmitter(emitterPackage, EmitOutputDir / outputSubdir);

    void RunTypeSpecEmitter(string emitterPackage, AbsolutePath outDir)
    {
        var spec = DomainSpecRoot;
        EnsureCleanDirectory(outDir);

        // `npm exec --no -- tsp ...` runs the locally-installed TypeSpec compiler
        // without npm attempting to install missing binaries (`--no` is npm's
        // short form of `--no-install`). The explicit emitter option is required:
        // tspconfig.yaml's per-emitter output path otherwise wins over --output-dir.
        NpmTasks.Npm(
            $"exec --no -- tsp compile main.tsp --emit \"{emitterPackage}\" --option \"{emitterPackage}.emitter-output-dir={outDir}\"",
            workingDirectory: spec);
    }

    // ---------------------------------------------------------------------
    // VerifyEmitDeterministic: run EmitAll twice into separate scratch dirs and diff.
    // ---------------------------------------------------------------------
    Target VerifyEmitDeterministic => _ => _
        .Description("Run every emitter twice into Artifacts/emit-a and Artifacts/emit-b; fail on any byte difference.")
        .DependsOn(VerifyKeysLockstep)
        .Executes(() =>
        {
            var dirA = ArtifactsDir / "emit-a";
            var dirB = ArtifactsDir / "emit-b";

            RunEmittersInto(dirA);
            RunEmittersInto(dirB);

            var (ok, firstDiff) = DiffDirectoriesBytewise(dirA, dirB);
            if (!ok)
            {
                var msg = $"VerifyEmitDeterministic: drift detected at '{firstDiff}' between '{dirA}' and '{dirB}'.";
                throw new InvalidOperationException(msg);
            }

            Log.Information("VerifyEmitDeterministic: '{A}' and '{B}' are byte-identical across {Count} emitter(s).",
                dirA, dirB, Emitters.Length);
        });

    void RunEmittersInto(AbsolutePath root)
    {
        var spec = DomainSpecRoot;
        EnsureCleanDirectory(root);

        foreach (var emitter in Emitters)
        {
            var (package, subdir) = ResolveEmitter(emitter);
            var outDir = root / subdir;
            EnsureCleanDirectory(outDir);
            NpmTasks.Npm(
                $"exec --no -- tsp compile main.tsp --emit \"{package}\" --option \"{package}.emitter-output-dir={outDir}\"",
                workingDirectory: spec);
        }
    }

    static (string Package, string Subdir) ResolveEmitter(string emitter) => emitter switch
    {
        "csharp" => ("@ancplua/typespec-emit-csharp", "csharp"),
        "ts-types" => ("@ancplua/typespec-emit-ts-types", "ts-types"),
        _ => throw new ArgumentException($"Unknown emitter '{emitter}'. Expected: csharp or ts-types.", nameof(emitter)),
    };

    static (bool Ok, string? FirstDiff) DiffDirectoriesBytewise(AbsolutePath a, AbsolutePath b)
    {
        var filesA = Directory.Exists(a)
            ? Directory.GetFiles(a, "*", SearchOption.AllDirectories).Select(p => Path.GetRelativePath(a, p).Replace('\\', '/')).OrderBy(p => p, StringComparer.Ordinal).ToArray()
            : Array.Empty<string>();
        var filesB = Directory.Exists(b)
            ? Directory.GetFiles(b, "*", SearchOption.AllDirectories).Select(p => Path.GetRelativePath(b, p).Replace('\\', '/')).OrderBy(p => p, StringComparer.Ordinal).ToArray()
            : Array.Empty<string>();

        if (!filesA.SequenceEqual(filesB, StringComparer.Ordinal))
        {
            var onlyInA = filesA.Except(filesB, StringComparer.Ordinal).FirstOrDefault();
            var onlyInB = filesB.Except(filesA, StringComparer.Ordinal).FirstOrDefault();
            return (false, onlyInA ?? onlyInB ?? "<file-set differs>");
        }

        foreach (var rel in filesA)
        {
            var bytesA = File.ReadAllBytes(Path.Combine(a, rel));
            var bytesB = File.ReadAllBytes(Path.Combine(b, rel));
            if (!bytesA.AsSpan().SequenceEqual(bytesB))
            {
                return (false, rel);
            }
        }

        return (true, null);
    }

    static void EnsureCleanDirectory(AbsolutePath path)
    {
        if (Directory.Exists(path))
        {
            Directory.Delete(path, recursive: true);
        }
        Directory.CreateDirectory(path);
    }

    // ---------------------------------------------------------------------
    // BuildCSharpEmit: dotnet build the emitted csproj (if any).
    // ---------------------------------------------------------------------
    Target BuildCSharpEmit => _ => _
        .Description("dotnet build the emitted C# project under Artifacts/emit/csharp (no-op when no csproj is emitted).")
        .DependsOn(EmitCSharp)
        .Executes(() =>
        {
            var csharpOut = EmitOutputDir / "csharp";
            if (!Directory.Exists(csharpOut))
            {
                Log.Warning("BuildCSharpEmit: '{Dir}' does not exist — skipping.", csharpOut);
                return;
            }

            var projects = Directory.GetFiles(csharpOut, "*.csproj", SearchOption.AllDirectories);
            if (projects.Length == 0)
            {
                Log.Warning("BuildCSharpEmit: no *.csproj under '{Dir}' — skipping.", csharpOut);
                return;
            }

            foreach (var csproj in projects)
            {
                DotNetBuild(s => s
                    .SetProjectFile(csproj)
                    .SetConfiguration("Release"));
            }
        });

    // ---------------------------------------------------------------------
    // VerifyGeneratedArtifactsCurrent: regenerate the repository-owned tracked
    // OpenAPI artifact into scratch and compare it byte-for-byte with the
    // working tree. This proves provenance without requiring a clean checkout,
    // which would reject every legitimate pre-commit schema change.
    // ---------------------------------------------------------------------
    Target VerifyGeneratedArtifactsCurrent => _ => _
        .Description("Regenerate tracked OpenAPI output into scratch and fail on drift or unexpected untracked generated files.")
        .DependsOn(CompileDomainSpec)
        .Executes(() =>
        {
            var generated = DomainSpecRoot / "generated";
            var expectedOpenApi = generated / "openapi";
            var actualOpenApi = ArtifactsDir / "verify-generated" / "openapi";

            EnsureCleanDirectory(actualOpenApi);
            NpmTasks.Npm(
                $"exec --no -- tsp compile main.tsp --emit \"@typespec/openapi3\" --option \"@typespec/openapi3.emitter-output-dir={actualOpenApi}\"",
                workingDirectory: DomainSpecRoot);

            var (ok, firstDiff) = DiffDirectoriesBytewise(expectedOpenApi, actualOpenApi);
            if (!ok)
            {
                throw new InvalidOperationException(
                    $"VerifyGeneratedArtifactsCurrent: tracked OpenAPI output is stale at '{firstDiff}'. Run `npm run compile` and commit the regenerated artifact.");
            }

            // Ignored emitter/runtime outputs are package inputs and are rebuilt by
            // prepack. Non-ignored untracked files have no declared owner and must
            // not silently enter generated/.
            var untracked = Git($"ls-files --others --exclude-standard -- \"{generated}\"", workingDirectory: DomainSpecRoot, logOutput: false)
                .Select(l => l.Text)
                .Where(l => !string.IsNullOrWhiteSpace(l))
                .ToArray();
            if (untracked.Length > 0)
            {
                throw new InvalidOperationException(
                    $"VerifyGeneratedArtifactsCurrent: {untracked.Length} unexpected untracked file(s) under '{generated}':"
                    + Environment.NewLine
                    + string.Join(Environment.NewLine, untracked));
            }

            Log.Information("VerifyGeneratedArtifactsCurrent: tracked OpenAPI output matches a fresh emit.");
        });

    Target VerifyRouteContracts => _ => _
        .Description("Assert implemented route guards and typed-query validation failures are declared in OpenAPI.")
        .DependsOn(VerifyGeneratedArtifactsCurrent)
        .Executes(() =>
        {
            NpmRun(s => s
                .SetCommand("verify:routes")
                .SetProcessWorkingDirectory(DomainSpecRoot));
        });

    // ---------------------------------------------------------------------
    // PackApiPackage: npm pack
    // ---------------------------------------------------------------------
    Target PackApiPackage => _ => _
        .Description("Run `npm pack` to validate package.json#files and produce the tarball.")
        .DependsOn(
            VerifyKeysLockstep,
            VerifyEmitDeterministic,
            VerifyGeneratedArtifactsCurrent,
            CompileDomainSpec)
        .Executes(() =>
        {
            var spec = DomainSpecRoot;
            NpmTasks.Npm("pack", workingDirectory: spec);
        });

    // ---------------------------------------------------------------------
    // PackContractsNuget: the C# contracts NuGet,
    // mirroring the npm Pack target. The packaging/ project
    // compiles generated/contracts (gitignored @ancplua/typespec-emit-csharp
    // output) into Qyl.Api.Contracts, versioned from package.json so
    // the npm and NuGet artifacts stay in lockstep.
    // ---------------------------------------------------------------------
    AbsolutePath PackagingProject => RootDirectory / "packaging" / "Qyl.Api.Contracts.csproj";
    AbsolutePath NugetOutputDir => ArtifactsDir / "nuget";

    AbsolutePath ContractsEmitDir => DomainSpecRoot / "generated" / "contracts";

    // NuGet-compatible SemVer (MAJOR.MINOR.PATCH[-prerelease]); no build metadata.
    static readonly Regex NugetVersionPattern =
        new(@"^\d+\.\d+\.\d+(-[A-Za-z0-9][A-Za-z0-9.-]*)?$", RegexOptions.Compiled);

    string PackageJsonVersion()
    {
        var pkgJson = DomainSpecRoot / "package.json";
        using var doc = JsonDocument.Parse(File.ReadAllText(pkgJson));
        return doc.RootElement.GetProperty("version").GetString()
            ?? throw new InvalidOperationException($"'{pkgJson}' has no 'version'.");
    }

    // NuGet rejects npm-legal build metadata (e.g. 1.2.3+build.5) and other forms package.json
    // may carry. Strip build metadata and require NuGet-compatible SemVer, so a version that
    // publishes cleanly on npm cannot hard-fail the pack and silently break the npm/NuGet lockstep.
    string NugetPackageVersion()
    {
        var raw = PackageJsonVersion();
        var version = raw.Split('+', 2)[0];
        if (!NugetVersionPattern.IsMatch(version))
            throw new InvalidOperationException(
                $"NugetPackageVersion: package.json version '{raw}' is not NuGet-compatible (expected MAJOR.MINOR.PATCH[-prerelease]).");
        return version;
    }

    // Clean generated/contracts before the package-specific emit. The normal EmitCSharp target
    // writes to Artifacts/emit/csharp; the NuGet path is deliberately separate so deterministic
    // verification and package assembly cannot accidentally inspect the same directory.
    Target CleanContractsEmit => _ => _
        .Unlisted()
        .Executes(() => ContractsEmitDir.CreateOrCleanDirectory());

    Target PackContractsNuget => _ => _
        .Description("Pack the freshly-emitted C# contracts (generated/contracts) into the Qyl.Api.Contracts NuGet (versioned from package.json).")
        .DependsOn(CleanContractsEmit, CompileDomainSpec)
        .Executes(() =>
        {
            RunTypeSpecEmitter("@ancplua/typespec-emit-csharp", ContractsEmitDir);

            // Fail fast rather than ship an empty assembly if the emit produced nothing.
            if (ContractsEmitDir.GlobFiles("**/*.cs").Count == 0)
                throw new InvalidOperationException(
                    $"PackContractsNuget: no *.cs under '{ContractsEmitDir}' after emit — refusing to pack an empty package.");

            NugetOutputDir.CreateOrCleanDirectory();
            DotNetPack(s => s
                .SetProject(PackagingProject)
                .SetConfiguration("Release")
                .SetOutputDirectory(NugetOutputDir)
                .SetVersion(NugetPackageVersion()));
        });

    // ---------------------------------------------------------------------
    // Local "everything green" gate.
    // ---------------------------------------------------------------------
    Target Check => _ => _
        .Description("Local everything-green gate: restore -> lockstep -> compile -> generated-current -> emit-all -> determinism -> npm pack -> C# contracts pack.")
        .DependsOn(
            RestoreTypeSpecDeps,
            VerifyKeysLockstep,
            CompileDomainSpec,
            VerifyGeneratedArtifactsCurrent,
            VerifyRouteContracts,
            EmitAll,
            VerifyEmitDeterministic,
            PackApiPackage,
            PackContractsNuget);
}
